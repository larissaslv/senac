n= int(input ("Digite um número: "))
# Se n for maior que 10, será imprimido o valor até o valor digitado.
if n > 10:
    for i in range (11,n):
        print (f"{i}")
#para que a repetição seja por exemplo de 2 em 2 só fazer assim: for i in range (11,n,2)
#Precisa colocar uma vírgula logo depois da variável e a quantidade que deseja que seja repetida.
    
else:
    print (f"{10-n}")

